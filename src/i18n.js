// Safely get locale
const getLocale = () => {
  try {
    // Use getLocales() for expo-localization v14+
    const locales = Localization.getLocales();
    if (locales && locales.length > 0 && locales[0].languageCode) {
      return locales[0].languageCode;
    }
  } catch (_e) {
    if (__DEV__) {
      console.info('Locale detection failed, using default');
    }
  }
  return 'en';
};

i18n
  .use(initReactI18next)
  .init({
    compatibilityJSON: 'v3',
    resources,
    lng: getLocale(),
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false,
    },
  });

// Switch layout direction when language changes (RTL for Arabic)
i18n.on('languageChanged', (lng) => {
  const isRTL = lng === 'ar';
  if (I18nManager.isRTL !== isRTL) {
    try {
      I18nManager.allowRTL(isRTL);
      I18nManager.forceRTL(isRTL);
    } catch (_e) {
      // ignore
    }
  }
});

export default i18n;
